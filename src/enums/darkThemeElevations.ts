export interface IElevationProps {
  backgroundImage: string;
  boxShadow?: string;
}

export const setBackgroundImageProp = (elevation: DarkThemeElevationsAlpha) => {
  return {
    backgroundImage: `linear-gradient(rgba(255, 255, 255, ${elevation}), rgba(255, 255, 255, ${elevation}))`,
  };
};

export const setBoxShadowProp = (
  elevation: DarkThemeElevationShadows,
  overrideDefaultBoxShadow?: string
) => {
  if (overrideDefaultBoxShadow) {
    return { boxShadow: `${overrideDefaultBoxShadow}` };
  }
  return {
    boxShadow: `${elevation}`,
  };
};

